# Aritmatika
x = 9
y = 3

# Penjumlahan +
hasil = x + y
print(x, '+' ,y ,'=' , hasil)

# Pengurangan -
hasil = x - y
print(x, '-' ,y ,'=' , hasil)

# Perkalian x
hasil = x * y
print(x, 'x' ,y ,'=' , hasil)

# Perpangkatan **
hasil = x ** y
print(x, '' ,y ,'=' , hasil)

# Modulus %
hasil = x % y
print(x, 'mod' ,y ,'=' , hasil)

# Ploor division //
hasil = x // y
print(x, 'floor' ,y ,'=' , hasil)


# Skala prioritas (), **, */:, +/-
x = 3
y = 4
z = 5
hasil = (x ** y * (z + x) / y - z % x // y)
print(hasil)
print( x, '**', y, '*', z, '+', x, '/', y, '-', z, '%', x, '//', y, '=', hasil)





